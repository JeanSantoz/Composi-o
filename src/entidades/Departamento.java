package entidades;

public class Departamento {
	
	// Atributos
	
	private String nome;

	// Construtores 
	
	public Departamento() {}
	
	public Departamento(String nome) {
		this.nome = nome;
	}

	// gets and sets
	
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	

}
